const { User } = require('../../../models');

/** 
 * Count users based on query
 * @param {Object} query
 * @returns {Promise}
 */
async function countUsers(query) {
  return User.countDocuments(query);
}

/**
 * Get users based on query
 * @param {Object} query - fungsi query
 * @param {Object} sorting - fungsi sorting
 * @param {number} skip - fungsi skip
 * @param {number} limit - fungsi limit
 * @returns {Promise}
 */
async function getUsers(query, sorting, skip, limit) {
  return User.find(query).sort(sorting).skip(skip).limit(limit);
}

/**
 * Get user detail
 * @param {string} id - User ID
 * @returns {Promise}
 */
async function getUser(id) {
  return User.findById(id);
}

/**
 * Create new user
 * @param {string} name - Name
 * @param {string} email - Email
 * @param {string} password - Hashed password
 * @returns {Promise}
 */
async function createUser(name, email, password) {
  return User.create({
    name,
    email,
    password,
  });
}

/**
 * Update existing user
 * @param {string} id - User ID
 * @param {string} name - Name
 * @param {string} email - Email
 * @returns {Promise}
 */
async function updateUser(id, name, email) {
  return User.updateOne(
    {
      _id: id,
    },
    {
      $set: {
        name,
        email,
      },
    }
  );
}

/**
 * Delete a user
 * @param {string} id - User ID
 * @returns {Promise}
 */
async function deleteUser(id) {
  return User.deleteOne({ _id: id });
}

/**
 * Get user by email to prevent duplicate email
 * @param {string} email - Email
 * @returns {Promise}
 */
async function getUserByEmail(email) {
  return User.findOne({ email });
}

/**
 * Update user password
 * @param {string} id - User ID
 * @param {string} password - New hashed password
 * @returns {Promise}
 */
async function changePassword(id, password) {
  return User.updateOne({ _id: id }, { $set: { password } });
}

module.exports = {
  getUsers,
  getUser,
  createUser,
  countUsers,
  updateUser,
  deleteUser,
  getUserByEmail,
  changePassword,
};
